typedef struct
{
    fract16         *h;     /*  filter coefficients            */
    complex_fract16 *d;     /*  start of delay line            */
    complex_fract16 *p;     /*  read/write pointer             */
    int             k;      /*  number of coefficients         */
} fir_state;

typedef struct
{
	int k;          /*  AGC_ADJ_STEP  */
    int l;          /*  AGC_REF_POWER */
} agc_state;

typedef struct
{
    int k;          /*  number of sin/cos coefficient  */
    int16_t *d;     /*  pointer to sin/cos table       */
} ddc_state;

typedef struct
{
	complex_fract16 *d;     /*  start of delay line            */
	complex_fract16 *p;     /*  read/write pointer             */
    int             h;      /*  delay buffer depth             */
    int             l;      /*  seg length                     */
} rsm_state;

#define fir1_init(state, coeffs, delay, ncoeffs) \
    do {                      \
       (state).h = (coeffs);  \
       (state).d = (delay);   \
       (state).p = (delay);   \
       (state).k = (ncoeffs); \
    } while (0)

#define agc_init(state, adjstep, refpwr) \
    do {                      \
       (state).k = (adjstep); \
       (state).l = (refpwr);   \
    } while (0)

#define ddc_init(state, nlen, coefT) \
	do {						    \
	    (state).k = (nlen);         \
	    (state).d = (coefT);         \
    } while(0)

#define rsm_init(state, delay, nDepth, segLen) \
	do {						    \
	    (state).d = (delay);        \
	    (state).p = (delay);        \
        (state).h = (nDepth);       \
        (state).l = (segLen);       \
    } while(0)

